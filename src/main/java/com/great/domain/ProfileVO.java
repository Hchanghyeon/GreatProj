package com.great.domain;


public class ProfileVO {
	private String username;
	private int age;
	private int height;
	private String gender;
	private String phone;
	private String allergyinfo;
	private String nation_nm;
	private String ty_nm;
	private String level_nm;
	private String id;
	private String path;


	public String getUsername() {
		return username;
	}



	public void setUsername(String username) {
		this.username = username;
	}



	public int getAge() {
		return age;
	}



	public void setAge(int age) {
		this.age = age;
	}



	public int getHeight() {
		return height;
	}



	public void setHeight(int height) {
		this.height = height;
	}



	public String getGender() {
		return gender;
	}



	public void setGender(String gender) {
		this.gender = gender;
	}



	public String getPhone() {
		return phone;
	}



	public void setPhone(String phone) {
		this.phone = phone;
	}



	public String getAllergyinfo() {
		return allergyinfo;
	}



	public void setAllergyinfo(String allergyinfo) {
		this.allergyinfo = allergyinfo;
	}





	public String getNation_nm() {
		return nation_nm;
	}



	public void setNation_nm(String nation_nm) {
		this.nation_nm = nation_nm;
	}



	public String getTy_nm() {
		return ty_nm;
	}



	public void setTy_nm(String ty_nm) {
		this.ty_nm = ty_nm;
	}



	public String getLevel_nm() {
		return level_nm;
	}



	public void setLevel_nm(String level_nm) {
		this.level_nm = level_nm;
	}



	public String getId() {
		return id;
	}



	public void setId(String id) {
		this.id = id;
	}



	public String getPath() {
		return path;
	}



	public void setPath(String path) {
		this.path = path;
	}



}
